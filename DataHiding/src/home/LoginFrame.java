    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:

        String username = usernameTextField.getText().toString();
        String password = passwordTextField.getText().toString();

        if (username.equals("") || username == null || password.equals("") || password == null) {
            JOptionPane.showMessageDialog(this, "Userame or password can not be empty");
        } else {
            DataBaseHelper baseHelper = DataBaseHelper.getDbCon();
            Connection connection = baseHelper.conn;
            if (connection == null) {
                JOptionPane.showMessageDialog(this, "Error in establishing connection");
            } else {
                try {
                    String query = "select * from login where username='" + username + "'and password = '" + password + "'";
                    ResultSet resultSet = baseHelper.getResult(query);

                    if (resultSet.next()) {
                        JOptionPane.showMessageDialog(this, "Login Successful");
                        new Main().setVisible(true);
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Login Failed");
                        usernameTextField.setText("");
                        passwordTextField.setText("");
                    }
                } catch (Exception e) {
                }
            }
        }
    }//GEN-LAST:event_loginButtonActionPerformed

